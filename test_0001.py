# Simple function that returns sum of it's two arguemnts
# We assume that they are of 'float' type, although they may
# actually be of many other types (int, list, str, tuple, or
# anything with the method __add__ defined.)
# 
# The return value is an expression, we create a new variable
# for it in the translation.

def add0001(a, b):
    return a + b
